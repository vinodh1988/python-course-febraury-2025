# Program to demonstrate various number literals

# Integer literals
decimal_literal = 10
binary_literal = 0b1010 # 10--> 1010 = 8+2 = 10
octal_literal = 0o12 # 10
hexadecimal_literal = 0x12A # 256+32+10 = 298

print("Decimal Literal:", decimal_literal)
print("Binary Literal:", binary_literal)
print("Octal Literal:", octal_literal)
print("Hexadecimal Literal:", hexadecimal_literal)

# Floating-point literals
float_literal = 10.5
