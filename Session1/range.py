# Demonstrating the use of range() without any loop statement

# Creating a range object
range_obj = range(10)

# Converting range object to a list
range_list = list(range_obj)

# Printing the list
print(range_list)

# Creating a range object with start, stop, and step arguments
range_obj_with_step = range(1, 10, 2)

# Converting range object to a list
range_list_with_step = list(range_obj_with_step)

# Printing the list
print(range_list_with_step)

# Creating a range object with start, stop, and step arguments in reverse
range_obj_reverse = range(10, 1, -2)

# Converting range object to a list
range_list_reverse = list(range_obj_reverse)

# Printing the list
print(range_list_reverse)