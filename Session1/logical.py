# Demonstrating logical operators in Python

# and operator
a = True
b = False
print("a and b:", a and b)  # Output: False

# or operator
print("a or b:", a or b)  # Output: True

# not operator
print("not a:", not a)  # Output: False
print("not b:", not b)  # Output: True

# Combining logical operators
x = 5
y = 10
print("x > 3 and y < 15:", x > 3 and y < 15)  # Output: True
print("x < 3 or y > 15:", x < 3 or y > 15)  # Output: False
print("not (x == 5):", not (x == 5))  # Output: False